export function statusCodeIsRedirect(code: StatusCode): boolean {
    return code >= StatusCode.MULTIPLE_CHOICES && code < StatusCode.BAD_REQUEST;
}

export function statusCodeIsError(code: StatusCode): boolean {
    return code >= StatusCode.INTERNAL_SERVER_ERROR;
}

export function statusCodeAsPhrase(code: StatusCode): StatusText {
    return statusCodeToText[code];
}
